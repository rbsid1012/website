const SidebarMenuButton = React.forwardRef<
  HTMLButtonElement,
  React.ComponentProps<"button"> & {
    asChild?: boolean
    isActive?: boolean
    tooltip?: string | React.ComponentProps<typeof TooltipContent>
    icon?: React.ElementType; // New prop to pass the Lucide icon component
  } & VariantProps<typeof sidebarMenuButtonVariants>
>(
  (
    {
      asChild = false,
      isActive = false,
      variant = "default",
      size = "default",
      tooltip,
      icon: Icon, // Destructure the new icon prop
      className,
      children, // This will be the text label for the menu item
      ...props
    },
    ref
  ) => {
    const Comp = asChild ? Slot : "button"
    const { isMobile, state } = useSidebar()

    // Determine if we are in the "icon-only" display mode (collapsed desktop sidebar)
    const isIconOnlyState = state === "collapsed" && !isMobile;

    const buttonContent = (
      <>
        {/* Icon: Visible when expanded, or when collapsed on desktop (icon-only mode) */}
        {Icon && (
          <Icon
            className={cn(
              "size-4 shrink-0",
              // Hide icon when expanded on desktop
              state === "expanded" && !isMobile && "hidden"
            )}
          />
        )}
        {/* Text: Visible when expanded, or always on mobile */}
        <span
          className={cn(
            "truncate",
            // Hide text when collapsed on desktop
            state === "collapsed" && !isMobile && "hidden"
          )}
        >
          {children}
        </span>
      </>
    );

    const button = (
      <Comp
        ref={ref}
        data-sidebar="menu-button"
        data-size={size}
        data-active={isActive}
        className={cn(
          sidebarMenuButtonVariants({ variant, size }),
          // Adjust button styling for icon-only state: center content, force icon size
          isIconOnlyState ? "justify-center !size-8 !p-2" : "justify-start",
          className
        )}
        {...props}
      >
        {buttonContent}
      </Comp>
    );

    // Tooltip logic: only show when in icon-only state (collapsed desktop)
    if (!tooltip) {
      return button
    }

    if (typeof tooltip === "string") {
      tooltip = {
        children: tooltip,
      }
    }

    return (
      <Tooltip>
        <TooltipTrigger asChild>{button}</TooltipTrigger>
        <TooltipContent
          side="right"
          align="center"
          hidden={!isIconOnlyState} // Tooltip only when collapsed and not mobile
          {...tooltip}
        />
      </Tooltip>
    )
  }
)
SidebarMenuButton.displayName = "SidebarMenuButton"

